def perfil_pessoal():
    print("\n Perfil Pessoal")
    ocupacao = input("Ocupação: ")
    idade = input("Idade: ")
    estado_civil = input("Estado Civil: ")
    objetivo = input("Objetivo: ")

    print("\nDados do Perfil Pessoal")
    print(f"Ocupação: {ocupacao}")
    print(f"Idade: {idade}")
    print(f"Estado Civil: {estado_civil}")
    print(f"Objetivo: {objetivo}")

perfil_pessoal()
